export type MessageKey = keyof typeof messages.en

export function t(locale: Locale, key: MessageKey, values?: Record<string, MessageValue>): string {
  const template = messages[locale][key] ?? messages.en[key]
  if (!values) {
    return template
  }
  return template.replace(/\{(\w+)\}/g, (_match, token) => String(values[token] ?? `{${token}}`))
}
